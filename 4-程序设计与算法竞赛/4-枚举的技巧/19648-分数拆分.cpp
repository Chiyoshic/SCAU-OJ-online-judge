#include <stdio.h>
/*
数学推导理解：

从等式1/k = 1/x + 1/y出发，可以推导出x = (k*y)/(y-k)

为了保证x是整数，(k*y)必须能被(y-k)整除

题目要求x >= y，即(k*y)/(y-k) >= y
*/

int main() {
    long k, y, x;  // k:输入的正整数，i和j:用于计算的两个变量
    
    // 读取输入的正整数k
    scanf("%ld", &k);
    
    // 遍历y的可能取值范围：k+1到2k
    for(y = k + 1; y <= 2 * k; y++)
    {
        // 计算j的值：j = (k*i)/(i-k)
        x = k * y / (y - k);
        
        // 检查两个条件：
        // 1. (k*i)是否能被(i-k)整除（保证j是整数）
        // 2. j是否大于等于i（保证x>=y，即j>=i）
        if(k * y % (y - k) == 0 && x >= y)
            // 输出满足条件的等式，格式为1/k=1/x+1/y
            printf("1/%ld=1/%ld+1/%ld\n", k, x, y);
    }
    
    return 0;
}
/*
描述：
输入一个正整数，找到所有的正整数x>=y,使得1/k=1/x+1/y

输入格式：
一个正整数n，n<=100000

输出格式：
一行一个分式等式
小的分数显示在前面；多个等式，等式右边第一个分数小的先输出

输入样例：
2

输出样例：
1/2=1/6+1/3
1/2=1/4+1/4
*/